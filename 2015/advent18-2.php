$lines = explode("\n", $input);

$matrix = [];
$row = 0;

foreach ($lines as $line) {
    $chars = str_split($line);
    $i = 0;
    foreach ($chars as $char) {
        if ($char == '#' || $char == '.') {
            $on = true;
            if ($char == '.') {
                $on = false;
            }
            $matrix[$row . 'x' . $i] = $on;
            $i++;
        }
    }
    $row++;
}

function neighborStatus($position) {
    global $matrix;

    list($x, $y) = explode('x', $position);

    $neighbors = [
        ($x - 1) . 'x' . ($y - 1),
        ($x - 1) . 'x' . ($y),
        ($x - 1) . 'x' . ($y + 1),
        ($x) . 'x' . ($y - 1),
        ($x) . 'x' . ($y + 1),
        ($x + 1) . 'x' . ($y - 1),
        ($x + 1) . 'x' . ($y),
        ($x + 1) . 'x' . ($y + 1),
    ];

    $result = ['on' => 0, 'off' => 0];

    foreach ($neighbors as $neighbor) {
        if (isset($matrix[$neighbor])) {
            if ($matrix[$neighbor] === true) {
                $result['on']++;
            } else {
                $result['off']++;
            }
        } else {
            $result['off']++;
        }
    }

    return $result;
}

for ($i = 0; $i < 100; $i++) {
    $changes = [];
    foreach ($matrix as $coordinate => $status) {
        $neighbors = neighborStatus($coordinate);

        if ($status === true) {
            if ($neighbors['on'] == 2 || $neighbors['on'] == 3) {
                // do nothing
            } else {
                $changes[] = $coordinate;
            }
        } else {
            if ($neighbors['on'] == 3) {
                $changes[] = $coordinate;
            }
        }
    }

    // Apply changes atomically
    foreach ($changes as $change) {
        if ($change != '0x0' && $change != '0x99' && $change != '99x0' && $change != '99x99') {
        //if ($change != '0x0' && $change != '0x5' && $change != '5x0' && $change != '5x5') {
            $matrix[$change] = !($matrix[$change]);
        }
    }
}

